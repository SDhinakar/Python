# "in" and "not in" are membership operators used to test if a value is in a sequence (string, list, tuple, set and dictionary)
# it is used to check if a value is present in a sequence or not

array=[-3,-2-1,0,1,2,3]
print(-3 in array)

string= 'Dhinakar Shanmugam'
print('Shanmugam' in string)

